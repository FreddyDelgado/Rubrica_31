        grafica.fillRect(0, 0, ancho, alto);
        grafica.setColor(new Color(198, 201, 206));
        grafica.drawLine(0, (1*alto)/10, ancho,(1*alto)/10);
        grafica.drawLine(0, (2*alto)/10, ancho,(2*alto)/10);
        grafica.drawLine(0, (3*alto)/10, ancho,(3*alto)/10);
        grafica.drawLine(0, (4*alto)/10, ancho,(4*alto)/10);
        grafica.drawLine(0, (5*alto)/10, ancho,(5*alto)/10);
        grafica.drawLine(0, (6*alto)/10, ancho,(6*alto)/10);
        grafica.drawLine(0, (7*alto)/10, ancho,(7*alto)/10);
        grafica.drawLine(0, (8*alto)/10, ancho,(8*alto)/10);
        grafica.drawLine(0, (9*alto)/10, ancho,(9*alto)/10);
        grafica.setColor(Color.black);
        grafica.drawString("0.9",ancho-30, (1*alto)/10);
        grafica.drawString("0.8",ancho-30, (2*alto)/10);
        grafica.drawString("0.7",ancho-30, (3*alto)/10);
        grafica.drawString("0.6",ancho-30, (4*alto)/10);
        grafica.drawString("0.5",ancho-30, (5*alto)/10);
        grafica.drawString("0.4",ancho-30, (6*alto)/10);
