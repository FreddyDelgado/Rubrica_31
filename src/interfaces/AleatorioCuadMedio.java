    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int semilla=Integer.parseInt(jtxtSemilla.getText());
        int digitos=Integer.parseInt(jComboDigitos.getSelectedItem().toString());
        int cant=Integer.parseInt(jComboCant.getSelectedItem().toString());
        
        generarTabla(cant);
        
        CuadradosMedios ran = new CuadradosMedios(semilla, digitos, cant);
        
        generado = ran.generar();
        int n = 0;
        for (double[] fila : generado) {
            tabla.setValueAt((int) fila[0], n, 0);
            tabla.setValueAt((int) fila[1], n, 1);
            tabla.setValueAt((int) fila[2], n, 2);
            tabla.setValueAt((int) fila[3], n, 3);
            tabla.setValueAt(fila[4], n, 4);
            n++;
        }
        tamano = generado.size();  
        generaGrafica(generado);
        
    }//GEN-LAST:event_jButton1ActionPerformed

